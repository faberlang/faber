#[test]
fn package_emit_honors_output_path() {
    let package = write_basic_package(
        "package-emit-output",
        r#"
incipit {
    nota "package emit"
}
"#,
    );
    let output = package.join("out/generated.rs");

    let (stdout, stderr, ok) = run_faber(&[
        "emit",
        "-t",
        "rust",
        "-o",
        output.to_str().expect("output path"),
        package.to_str().expect("package path"),
    ]);

    assert!(ok, "faber package emit failed: {stderr}");
    assert!(
        stdout.is_empty(),
        "emit -o should not write generated code to stdout: {stdout}"
    );
    let emitted = fs::read_to_string(&output).expect("read emitted rust");
    assert!(
        emitted.contains("fn main()"),
        "expected Rust main:\n{emitted}"
    );
    assert!(
        emitted.contains("package emit"),
        "expected package source in output:\n{emitted}"
    );
}

#[test]
fn package_check_diagnostics_uses_expanded_renderer() {
    let package = write_basic_package(
        "package-check-diagnostics",
        r#"
incipit {
    fixum numerus n ← "not a number"
    nota n
}
"#,
    );

    let (normal_stdout, normal_stderr, normal_ok) =
        run_faber(&["check", package.to_str().expect("package path")]);
    assert!(!normal_ok, "invalid package check should fail");
    assert!(normal_stdout.is_empty());
    // ALLOW_DIAGNOSTIC_RENDER_TEXT: CLI rendered diagnostic contract
    assert!(
        normal_stderr.contains("error[SEM010:"),
        "normal diagnostics should use coded compact headers:\n{normal_stderr}"
    );
    assert!(
        !normal_stderr.contains("\nphase:"),
        "normal package check must not render expanded records:\n{normal_stderr}"
    );

    let (expanded_stdout, expanded_stderr, expanded_ok) = run_faber(&[
        "check",
        "--diagnostics",
        package.to_str().expect("package path"),
    ]);
    assert!(
        !expanded_ok,
        "invalid package check --diagnostics should fail"
    );
    assert!(expanded_stdout.is_empty());
    // ALLOW_DIAGNOSTIC_RENDER_TEXT: CLI rendered diagnostic contract
    assert!(
        expanded_stderr.contains("error[")
            && expanded_stderr.contains("\nphase:")
            && expanded_stderr.contains("\nspan:"),
        "expanded diagnostics should include phase and span records:\n{expanded_stderr}"
    );
}

