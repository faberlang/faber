//! Subprocess checks for `faber emit`.

use std::fs;
use std::io::{Read, Write};
use std::path::PathBuf;
use std::process::{Command, Stdio};
use std::time::{SystemTime, UNIX_EPOCH};

fn run_faber_emit_faber_stdin(source: &str) -> String {
    let mut child = Command::new(env!("CARGO_BIN_EXE_faber"))
        .args(["emit", "-t", "faber", "-"])
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .expect("spawn faber emit");

    child
        .stdin
        .take()
        .expect("stdin")
        .write_all(source.as_bytes())
        .expect("write source");

    let mut stdout = String::new();
    child
        .stdout
        .take()
        .expect("stdout")
        .read_to_string(&mut stdout)
        .expect("read stdout");

    let mut stderr = String::new();
    if let Some(mut err) = child.stderr.take() {
        let _ = err.read_to_string(&mut stderr);
    }
    let status = child.wait().expect("wait");
    assert!(status.success(), "faber emit -t faber failed: {stderr}");
    stdout
}

fn corpus_root() -> std::path::PathBuf {
    std::path::Path::new(env!("CARGO_MANIFEST_DIR")).join("../examples/corpus")
}

fn reader_locale_example_root(locale: &str) -> PathBuf {
    std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("../examples/reader-locale")
        .join(locale)
}

fn temp_dir(label: &str) -> PathBuf {
    let nanos = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .expect("clock")
        .as_nanos();
    let dir = std::env::temp_dir().join(format!("faber-emit-integration-{label}-{nanos}"));
    fs::create_dir_all(&dir).expect("create temp dir");
    dir
}

fn write_plain_file(label: &str, contents: &str) -> PathBuf {
    let dir = temp_dir(label);
    let path = dir.join("plain.txt");
    fs::write(&path, contents).expect("write plain file");
    path
}

fn run_faber(args: &[&str]) -> (String, String, bool) {
    let mut child = Command::new(env!("CARGO_BIN_EXE_faber"))
        .args(args)
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .expect("spawn faber emit");

    let mut stdout = String::new();
    child
        .stdout
        .take()
        .expect("stdout")
        .read_to_string(&mut stdout)
        .expect("read stdout");

    let mut stderr = String::new();
    if let Some(mut err) = child.stderr.take() {
        let _ = err.read_to_string(&mut stderr);
    }
    let status = child.wait().expect("wait");
    (stdout, stderr, status.success())
}

fn run_faber_emit(args: &[&str]) -> (String, String, bool) {
    run_faber(args)
}

fn write_basic_package(label: &str, source: &str) -> PathBuf {
    let package = temp_dir(label);
    fs::create_dir_all(package.join("src")).expect("create src");
    fs::write(
        package.join("faber.toml"),
        r#"
[package]
name = "cli-test"

[paths]
source = "src"
entry = "main.fab"
"#,
    )
    .expect("write manifest");
    fs::write(package.join("src/main.fab"), source).expect("write entry");
    package
}

